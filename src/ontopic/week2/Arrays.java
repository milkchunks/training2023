package ontopic.week2;

public class Arrays {
    public static void main(String[] args) {
        //enhanced for loop
        int[] nums = {0, 1, 2, 3, 4, 5};
        //elm is i, and the for loop runs nums.length() times
        for (int elm : nums) {
            System.out.println(elm);
        }
    }
}
